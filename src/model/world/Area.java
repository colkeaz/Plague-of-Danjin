package model.world;

/**
 * Represents the distinct areas within the Dungeon of Danjin.
 * The dungeon is organized as a central hub (DANJINS_CORE) with
 * branching sub-areas the player can explore freely.
 */
public enum Area {
    DANJINS_CORE,
    GOBLIN_WARRENS,
    BONE_CATHEDRAL,
    PLAGUE_GARDENS,
    LICHS_THRONE
}
